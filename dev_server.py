#!/usr/bin/env python3
"""Nomeda — Development server with mock backend for frontend testing."""

from fastapi import FastAPI, WebSocket
from fastapi.staticfiles import StaticFiles
from fastapi.responses import FileResponse, StreamingResponse
import uvicorn, json, asyncio
import io, struct, time, math

app = FastAPI()

@app.get('/')
def root():
    return FileResponse('static/index.html')

@app.get('/admin')
def admin():
    return FileResponse('static/admin/index.html')

@app.websocket('/ws')
async def ws(websocket: WebSocket):
    await websocket.accept()
    try:
        while True:
            await websocket.send_json({
                'running': True,
                'video_emotion': 'Neutral',
                'voice_emotion': 'Neutral',
                'stt_text': '',
                'llm_response': 'Hello, how are you feeling?',
                'distress': 25,
                'tts_audio_url': None, 'tts_audio_mime': 'audio/wav',
                'tts_audio_b64': None, 'tts_generating': False
            })
            await asyncio.sleep(1)
    except Exception:
        pass

